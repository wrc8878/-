package pojo;

public class Bookin {
	private int biid;
	private int bkid;
	private String bkname;
	private String birate;
	private int bicount;
	private String bidate;
	public int getBiid() {
		return biid;
	}
	public void setBiid(int biid) {
		this.biid = biid;
	}
	public int getBkid() {
		return bkid;
	}
	public void setBkid(int bkid) {
		this.bkid = bkid;
	}
	public String getBkname() {
		return bkname;
	}
	public void setBkname(String bkname) {
		this.bkname = bkname;
	}
	public String getBirate() {
		return birate;
	}
	public void setBirate(String birate) {
		this.birate = birate;
	}
	public int getBicount() {
		return bicount;
	}
	public void setBicount(int bicount) {
		this.bicount = bicount;
	}
	public String getBidate() {
		return bidate;
	}
	public void setBidate(String bidate) {
		this.bidate = bidate;
	}
}
