--书籍分类表(booktype)
create table booktype(
bid int primary key identity,bname varchar(50))
--书籍详细信息表(bookinfo)
create table bookinfo(
bkid int primary key identity,bid int foreign key  references booktype(bid),
sid int foreign key  references supplierinfo(sid),
bkname varchar(50),bkconcern varchar(50),bkprice decimal(18,2),
bkwriter varchar(50),bkisdn varchar(50),
bkbarcord varchar(50),bkstatus int)
--书籍入库详细（流水）表（bookin）
create table bookin(
biid int primary key identity,bkid int foreign key references bookinfo(bkid),
birate varchar(50),bicount int,bidate smalldatetime)
--小区住户信息表（communityinfo）
create table communityinfo(
coid int primary key identity,
coname varchar(50),cocard varchar(18),coaddress varchar(50),
cophone varchar(11),libraryid varchar(9) unique)
--书籍详细借阅表(bookout)
create table bookout(
boid int primary key identity,
bkid int foreign key references bookinfo(bkid),
coid int foreign key references communityinfo(coid),
bocount int,bodate smalldatetime)
--书籍归还详细表（bookback）
create table bookback(
bbid int primary key identity,
boid int foreign key references bookout(boid),
bbdate smalldatetime not null)

