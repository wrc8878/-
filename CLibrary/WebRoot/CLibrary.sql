--员工表(emp)
create table emp(
eid int primary key identity,
ename varchar(12) not null,ecardid varchar(18) not null,ephone varchar(11) not null,
eaddress varchar(50) not null,ehiredate smalldatetime not null,eis int)
--员工登录表(userlogin)
create table userlogin(
uid int primary key identity,eid int foreign key  references emp(eid),
uname varchar(4),upass varchar(16))
--书籍供应商表(supplierinfo)
create table supplierinfo(
sid int primary key identity,sname varchar(50),saddress varchar(100),
slinkname varchar(50),sphone varchar(50))

